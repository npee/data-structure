package _06_Maze;

//*******************************************************
// Class Position.java
//*******************************************************

public class Position
{

    private int x;
    private int y;

    Position ()
    {
        x = 0;
        y = 0;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }

    public void setX(int a)
    {
        x = a;
    }

    public void setY(int a)
    {
        y = a;
    }
}
